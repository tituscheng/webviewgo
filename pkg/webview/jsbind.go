package webview

import (
	"fmt"
	"reflect"
	"strings"
)

// Binding represents a registered JS-to-Go function binding.
type Binding struct {
	Name string
	Fn   any
}

// GenerateTS produces TypeScript definitions for the given bindings.
func GenerateTS(bindings ...Binding) (string, error) {
	var b strings.Builder
	b.WriteString("// Generated by webviewgo. Do not edit.\n\n")
	b.WriteString("interface Window {\n")

	for _, binding := range bindings {
		ts, err := bindingToTS(binding)
		if err != nil {
			return "", fmt.Errorf("webview: GenerateTS for %q: %w", binding.Name, err)
		}
		b.WriteString("  ")
		b.WriteString(ts)
		b.WriteString("\n")
	}

	b.WriteString("}\n")
	return b.String(), nil
}

func bindingToTS(binding Binding) (string, error) {
	if binding.Name == "" {
		return "", fmt.Errorf("binding name is required")
	}
	if binding.Fn == nil {
		return "", fmt.Errorf("binding function is nil")
	}

	fnType := reflect.TypeOf(binding.Fn)
	if fnType.Kind() != reflect.Func {
		return "", fmt.Errorf("binding value is not a function")
	}

	var params []string
	for i := 0; i < fnType.NumIn(); i++ {
		paramType := goTypeToTS(fnType.In(i))
		params = append(params, fmt.Sprintf("arg%d: %s", i, paramType))
	}

	returnType := "void"
	numOut := fnType.NumOut()

	if numOut == 1 {
		if fnType.Out(0) == reflect.TypeOf((*error)(nil)).Elem() {
			returnType = "void"
		} else {
			returnType = goTypeToTS(fnType.Out(0))
		}
	} else if numOut >= 2 {
		// The last output is typically error; the rest are return values
		lastOut := fnType.Out(numOut - 1)
		if lastOut == reflect.TypeOf((*error)(nil)).Elem() {
			if numOut == 2 {
				returnType = goTypeToTS(fnType.Out(0))
			} else {
				// Multiple returns: tuple-like, represented as array in TS
				var types []string
				for i := 0; i < numOut-1; i++ {
					types = append(types, goTypeToTS(fnType.Out(i)))
				}
				returnType = "[" + strings.Join(types, ", ") + "]"
			}
		} else {
			var types []string
			for i := 0; i < numOut; i++ {
				types = append(types, goTypeToTS(fnType.Out(i)))
			}
			returnType = "[" + strings.Join(types, ", ") + "]"
		}
	}

	return fmt.Sprintf("%s(%s): Promise<%s>;", binding.Name, strings.Join(params, ", "), returnType), nil
}

func goTypeToTS(t reflect.Type) string {
	switch t.Kind() {
	case reflect.Int, reflect.Int8, reflect.Int16, reflect.Int32, reflect.Int64,
		reflect.Uint, reflect.Uint8, reflect.Uint16, reflect.Uint32, reflect.Uint64,
		reflect.Float32, reflect.Float64:
		return "number"
	case reflect.String:
		return "string"
	case reflect.Bool:
		return "boolean"
	case reflect.Slice, reflect.Array:
		return goTypeToTS(t.Elem()) + "[]"
	case reflect.Map:
		if t.Key().Kind() == reflect.String {
			return fmt.Sprintf("Record<string, %s>", goTypeToTS(t.Elem()))
		}
		return "Record<string, any>"
	case reflect.Struct:
		return goStructToTS(t)
	case reflect.Interface:
		if t.NumMethod() == 0 {
			return "any"
		}
		return "any"
	case reflect.Ptr:
		return goTypeToTS(t.Elem())
	case reflect.Func:
		return "Function"
	case reflect.Chan:
		return "any"
	default:
		return "any"
	}
}

func goStructToTS(t reflect.Type) string {
	if t.NumField() == 0 {
		return "object"
	}
	var fields []string
	for i := 0; i < t.NumField(); i++ {
		field := t.Field(i)
		if !field.IsExported() {
			continue
		}
		name := field.Name
		jsonTag := field.Tag.Get("json")
		if jsonTag != "" {
			parts := strings.Split(jsonTag, ",")
			if parts[0] != "" && parts[0] != "-" {
				name = parts[0]
			}
		}
		fields = append(fields, fmt.Sprintf("%s: %s", name, goTypeToTS(field.Type)))
	}
	return "{ " + strings.Join(fields, "; ") + " }"
}
